# 2. Конзолен конвертор: от радиани в градуси
# Напишете програма, която чете ъгъл в радиани (rad) и го преобразува в градуси (deg).  Принтирайте получените
# градуси като цяло число използвайки math.floor.
# Използвайте формулата: градуси = радиани * 180 / π.

import math

radians = float(input())
deg = radians * 180 / math.pi
print(math.floor(deg))

